function t(lang, key, params = {}) {
  const keys = key.split('.');
  let val = translations[lang] || translations.en;
  for (const k of keys) {
    val = val?.[k];
  }
  if (typeof val !== 'string') return key;
  return val.replace(/\{(\w+)\}/g, (_, p) => params[p] ?? `{${p}}`);
}

function getGreeting(lang) {
  const hour = new Date().getHours();
  if (hour < 12) return t(lang, 'greeting.morning');
  if (hour < 18) return t(lang, 'greeting.afternoon');
  return t(lang, 'greeting.evening');
}

module.exports = { translations, t, getGreeting };
